package mmorpg.mapreader;

/**
 *
 * @author Barrionuevo Diego
 */
public class PositionFile {

    private int x;
    private int y;

    public PositionFile(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
